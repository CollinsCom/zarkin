		<h4 style="font-size:16px;">Piel Anilina</h4>
        <img src="images/pieles/piel-anilina.jpg" width="141" height="146" style="float:left; padding:3px 10px 3px 0;" /><p>Es una de las más finas al tacto, lo cual la hace una de las más delicadas. Este acabado se logra cuando el color de anilina penetra por el poro, teniendo como ventaja que no cambia la superficie ni la tapa.</p>
		<p>Las variantes del color son inevitables ya que cada piel mantiene  su estructura particular.</p>
